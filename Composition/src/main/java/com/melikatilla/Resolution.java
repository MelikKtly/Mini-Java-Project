package com.melikatilla;

public class Resolution {
    private String boy;
    private String en;

    public Resolution(String boy, String en) {
        this.boy = boy;
        this.en = en;
    }
    public String getBoy() {
        return boy;
    }
    public void setBoy(String boy) {
        this.boy = boy;
    }
    public String getEn() {
        return en;
    }
    public void setEn(String en) {
        this.en = en;
    }


}
